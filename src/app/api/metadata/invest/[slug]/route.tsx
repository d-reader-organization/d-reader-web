import { fetchProject } from '@/app/lib/api/invest/queries'
import { join } from 'node:path'
import { readFile } from 'node:fs/promises'
import { ImageResponse } from 'next/og'
import { METADATA_IMAGE_SIZE } from '@/constants/general'

export async function GET(request: Request, props: { params: Promise<{ slug: string }> }) {
  const params = await props.params
  const project = (await fetchProject(params.slug)).data

  let bannerSrc = project?.banner

  if (bannerSrc?.includes('assets')) {
    const imagePath = join(process.cwd(), 'public', bannerSrc ?? '/assets/images/metadata-invest.png')
    const bannerData = await readFile(imagePath)
    const extension = bannerSrc.split('.')[1] === 'png' ? 'png' : 'jpeg'
    bannerSrc = `data:image/${extension};base64,${bannerData.toString('base64')}`
  }

  return new ImageResponse(
    (
      <div
        style={{
          fontSize: 48,
          backgroundColor: '#15171c',
          width: '100%',
          height: '100%',
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'center',
          color: 'white',
        }}
      >
        <img
          alt='Banner src'
          src={bannerSrc}
          style={{
            width: '100%',
            height: '100%',
            objectFit: 'fill',
          }}
        />
        <div
          style={{
            position: 'absolute',
            top: 0,
            left: 0,
            right: 0,
            bottom: 0,
            background:
              'linear-gradient(261deg, rgba(21, 23, 28, 0.00) 1.31%, rgba(21, 23, 28, 0.50) 49.87%, rgba(21, 23, 28, 0.89) 64.59%, #15171C 78.4%)',
          }}
        />
        <Logo />
        <p
          style={{
            position: 'absolute',
            left: 60,
            bottom: 140,
            fontSize: '56px',
            fontWeight: 600,
            lineHeight: 1.2,
            color: 'white',
            maxWidth: '1000px',
          }}
        >
          {project?.title}
        </p>
      </div>
    ),
    METADATA_IMAGE_SIZE
  )
}

